CharClass::Enum charClass(uint32_t ch)
{
    if (ch < 128)
        return (CharClass::Enum)(1 << AsciiCharClasses[ch]);
    else if (ch > 0xFFFFFFul)
        return CharClass::Unassigned;
    uint32_t key = ch << 8;
    uint32_t keyValue = *std::lower_bound(
            std::begin(CompleteCharClasses), std::end(CompleteCharClasses),
            key);
    return (CharClass::Enum)(1 << (keyValue & 0xFFul));
}

